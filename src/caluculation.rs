pub fn caluculate() {
    // 足し算
    let sum = 5 + 10;

    // 引き算
    let difference = 95.5 - 4.3;

    // 掛け算
    let product = 4 * 30;

    // 割り算 // note 浮動小数点のデフォルトはf64
    let quotient = 56.7 / 32.2;

    // 余り
    let reminder = 43 % 5;

    println!("足し算: {}", sum);
    println!("引き算: {}", difference);
    println!("掛け算: {}", product);
    println!("割り算: {}", quotient);
    println!("余り: {}", reminder);
}
